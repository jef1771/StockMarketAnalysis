 import java.util.Date;  

public class Day_Data {
	public float open;
	public float close;
	public float high;
	public float low;
	public double volume;
	public 
	
	public Day_Data(
			Date date,
			float open,
			float close,
			float high,
			float low,
			double volume
	)
	{
		this.date = date;
		this.open = open;
		this.close = close;
		this.high = high;
		this.low = low;
		this.volume = volume;
		this.date = date;
	}
}
