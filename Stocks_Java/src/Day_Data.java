
public class Day_Data {
	public float open;
	public float close;
	public float high;
	public float low;
	public double volume;
	
	public Day_Data(
			float open,
			float close,
			float high,
			float low,
			double volume
	)
	{
		this.open = open;
		this.close = close;
		this.high = high;
		this.low = low;
		this.volume = volume;
	}
}
